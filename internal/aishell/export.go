//go:embed scripts/bootstrap-tools.sh
var bootstrapToolsSh string

//go:embed scripts/setup-git-ssh.sh
var setupGitSshSh string

//go:embed scripts/Dockerfile.apt.tmpl
var dockerfileTmplApt string

//go:embed scripts/Dockerfile.dnf.tmpl
var dockerfileTmplDnf string

//go:embed scripts/Dockerfile.zypper.tmpl
var dockerfileTmplZypper string

//go:embed scripts/Dockerfile.pacman.tmpl
var dockerfileTmplPacman string

const (
	markerOpen  = "# === AI-SHELL AUTO-GENERATED — DO NOT EDIT BELOW THIS LINE ==="
	markerClose = "# === AI-SHELL AUTO-GENERATED — DO NOT EDIT ABOVE THIS LINE ==="
)

var dockerfileTmpls = map[string]string{
	"apt":    dockerfileTmplApt,
	"dnf":    dockerfileTmplDnf,
	"zypper": dockerfileTmplZypper,
	"pacman": dockerfileTmplPacman,
}

type dockerfileData struct {
	BaseImage string
}

// generateDockerfile renders the auto-generated section of the Dockerfile
// (including markers) for the given family and base image.
func generateDockerfile(baseImage, family string) (string, error) {
	tmplStr, ok := dockerfileTmpls[family]
	if !ok {
		return "", fmt.Errorf("no Dockerfile template for family %q", family)
	}
	t, err := template.New("Dockerfile").Parse(tmplStr)
	if err != nil {
		return "", fmt.Errorf("failed to parse Dockerfile template for %q: %w", family, err)
	}
	var buf bytes.Buffer
	if err := t.Execute(&buf, dockerfileData{BaseImage: baseImage}); err != nil {
		return "", fmt.Errorf("failed to render Dockerfile template for %q: %w", family, err)
	}

	var sb strings.Builder
	sb.WriteString(markerOpen + "\n")
	sb.WriteString(buf.String())
	sb.WriteString(markerClose + "\n")
	return sb.String(), nil
}

// defaultCustomSection is appended after the closing marker on first init.
func defaultCustomSection() string {
	return `
# Add your customizations below (extra packages, config, etc.).
# This section is preserved when you run 'ai-shell init --force'.
`
}

// spliceDockerfile replaces the auto-generated section in an existing Dockerfile
// while preserving everything outside the markers.
// Returns an error if the markers are not found.
func spliceDockerfile(existing, newAutoSection string) (string, error) {
	openIdx := strings.Index(existing, markerOpen)
	if openIdx < 0 {
		return "", fmt.Errorf("opening marker not found in existing Dockerfile")
	}
	closeIdx := strings.Index(existing, markerClose)
	if closeIdx < 0 {
		return "", fmt.Errorf("closing marker not found in existing Dockerfile")
	}
	closeEnd := closeIdx + len(markerClose)
	if closeEnd < len(existing) && existing[closeEnd] == '\n' {
		closeEnd++
	}

	before := existing[:openIdx]
	after := existing[closeEnd:]

	return before + newAutoSection + after, nil
}

// generateComposeOverrideYAML returns the docker-compose.override.yml content.
func generateComposeOverrideYAML() string {
	var sb strings.Builder
	sb.WriteString("# docker-compose.override.yml\n")
	sb.WriteString("# Add your project-specific customizations here.\n")
	sb.WriteString("# This file is merged with docker-compose.yml automatically by docker compose / podman-compose.\n")
	sb.WriteString("# It is NOT overwritten by 'ai-shell init' or 'ai-shell regen'.\n")
	sb.WriteString("#\n")
	sb.WriteString("# Examples:\n")
	sb.WriteString("#   services:\n")
	sb.WriteString("#     ai-shell:\n")
	sb.WriteString("#       environment:\n")
	sb.WriteString("#         - MY_VAR=value\n")
	sb.WriteString("#       volumes:\n")
	sb.WriteString("#         - /extra/path:/extra/path\n")
	sb.WriteString("#       ports:\n")
	sb.WriteString("#         - \"8080:8080\"\n")
	sb.WriteString("\n")
	sb.WriteString("services:\n")
	sb.WriteString("  ai-shell: {}\n")
	return sb.String()
}

// generateComposeYAML returns the docker-compose.yml content for export.
func generateComposeYAML(iid, container, image, volume, baseImage string) string {
	var sb strings.Builder

	sb.WriteString("# Generated by ai-shell — do not edit by hand.\n")
	sb.WriteString("# Use docker-compose.override.yml for customizations.\n")
	sb.WriteString("# Works with both docker compose and podman-compose\n")
	sb.WriteString("#\n")
	sb.WriteString("# Note: workdir is discovered from the /work bind mount (..:/work),\n")
	sb.WriteString("# so this file is portable across machines.\n")
	sb.WriteString("\n")
	sb.WriteString(fmt.Sprintf("name: ai-shell-%s\n", iid))
	sb.WriteString("\n")
	sb.WriteString("services:\n")
	sb.WriteString("  ai-shell:\n")
	sb.WriteString("    build:\n")
	sb.WriteString("      context: .\n")
	sb.WriteString("      args:\n")
	sb.WriteString(fmt.Sprintf("        - BASE_IMAGE=${BASE_IMAGE:-%s}\n", baseImage))
	sb.WriteString(fmt.Sprintf("    image: %s\n", image))
	sb.WriteString(fmt.Sprintf("    container_name: %s\n", container))
	sb.WriteString("    labels:\n")
	sb.WriteString(fmt.Sprintf("      %s: \"true\"\n", LabelManaged))
	sb.WriteString(fmt.Sprintf("      %s: \"1\"\n", LabelSchema))
	sb.WriteString(fmt.Sprintf("      %s: \"%s\"\n", LabelInstance, iid))
	sb.WriteString(fmt.Sprintf("      %s: \"%s\"\n", LabelVolume, volume))
	sb.WriteString("    volumes:\n")
	sb.WriteString("      - ..:/work                           # parent dir = workdir (source of truth)\n")
	sb.WriteString(fmt.Sprintf("      - %s:/root\n", volume))
	sb.WriteString("      - ${HOME}/.config/cursor:/root/.config/cursor:ro\n")
	sb.WriteString("      - ${HOME}/.claude:/root/.claude:ro\n")
	sb.WriteString("    env_file:\n")
	sb.WriteString("      - path: ${AI_SHELL_ENV_FILE:-/dev/null}\n")
	sb.WriteString("        required: false\n")
	sb.WriteString("    command: [\"sh\", \"-c\", \"while true; do sleep 3600; done\"]\n")
	sb.WriteString("\n")
	sb.WriteString("volumes:\n")
	sb.WriteString(fmt.Sprintf("  %s:\n", volume))
	sb.WriteString("    external: false\n")

	return sb.String()
}

