// exportFiles writes all export files to the output directory.
func exportFiles(outputDir, workdir string, cfg *Config, baseImage, family string, force bool) error {
	containerBase, imageBase, volumeBase := resolveBases(cfg)
	iid := InstanceID(workdir)
	container, volume := NamesFor(workdir, containerBase, volumeBase)
	image := imageBase + "-" + iid

	if err := os.MkdirAll(outputDir, 0o755); err != nil {
		return fmt.Errorf("failed to create output directory: %w", err)
	}

	// Files managed by init (checked for existence when not forcing).
	// docker-compose.override.yml is excluded — never overwritten regardless of --force.
	files := []string{"Dockerfile", "docker-compose.yml", "bootstrap-tools.sh", "setup-git-ssh.sh", "README.md"}
	if !force {
		var existing []string
		for _, f := range files {
			p := filepath.Join(outputDir, f)
			if _, err := os.Stat(p); err == nil {
				existing = append(existing, f)
			}
		}
		if len(existing) > 0 {
			return fmt.Errorf("files already exist in %s: %s\nUse --force to overwrite", outputDir, strings.Join(existing, ", "))
		}
	}

	// Generate Dockerfile auto-generated section
	autoSection, err := generateDockerfile(baseImage, family)
	if err != nil {
		return fmt.Errorf("failed to generate Dockerfile: %w", err)
	}

	dockerfilePath := filepath.Join(outputDir, "Dockerfile")
	if force {
		// Try to splice: preserve user customizations below the closing marker.
		existingContent, readErr := os.ReadFile(dockerfilePath)
		if readErr == nil {
			spliced, spliceErr := spliceDockerfile(string(existingContent), autoSection)
			if spliceErr == nil {
				if err := os.WriteFile(dockerfilePath, []byte(spliced), 0o644); err != nil {
					return fmt.Errorf("failed to write Dockerfile: %w", err)
				}
			} else {
				fmt.Fprintf(os.Stderr, "Warning: markers not found in existing Dockerfile; overwriting entirely.\n")
				if err := os.WriteFile(dockerfilePath, []byte(autoSection+defaultCustomSection()), 0o644); err != nil {
					return fmt.Errorf("failed to write Dockerfile: %w", err)
				}
			}
		} else {
			if err := os.WriteFile(dockerfilePath, []byte(autoSection+defaultCustomSection()), 0o644); err != nil {
				return fmt.Errorf("failed to write Dockerfile: %w", err)
			}
		}
	} else {
		if err := os.WriteFile(dockerfilePath, []byte(autoSection+defaultCustomSection()), 0o644); err != nil {
			return fmt.Errorf("failed to write Dockerfile: %w", err)
		}
	}

	// Generate and write docker-compose.yml
	composeContent := generateComposeYAML(iid, container, image, volume, baseImage)
	if err := os.WriteFile(filepath.Join(outputDir, "docker-compose.yml"), []byte(composeContent), 0o644); err != nil {
		return fmt.Errorf("failed to write docker-compose.yml: %w", err)
	}

	// Write docker-compose.override.yml only if it does not already exist.
	overridePath := filepath.Join(outputDir, "docker-compose.override.yml")
	if _, err := os.Stat(overridePath); os.IsNotExist(err) {
		overrideContent := generateComposeOverrideYAML()
		if err := os.WriteFile(overridePath, []byte(overrideContent), 0o644); err != nil {
			return fmt.Errorf("failed to write docker-compose.override.yml: %w", err)
		}
	}

	// Write helper scripts
	scripts := []struct {
		name    string
		content string
		perm    os.FileMode
	}{
		{"bootstrap-tools.sh", bootstrapToolsSh, 0o755},
		{"setup-git-ssh.sh", setupGitSshSh, 0o755},
	}
	for _, s := range scripts {
		dstPath := filepath.Join(outputDir, s.name)
		if err := os.WriteFile(dstPath, []byte(s.content), s.perm); err != nil {
			return fmt.Errorf("failed to write %s: %w", s.name, err)
		}
	}

	// Generate and write README.md
	readmeContent := generateReadme(container)
	if err := os.WriteFile(filepath.Join(outputDir, "README.md"), []byte(readmeContent), 0o644); err != nil {
		return fmt.Errorf("failed to write README.md: %w", err)
	}

	return nil
}
